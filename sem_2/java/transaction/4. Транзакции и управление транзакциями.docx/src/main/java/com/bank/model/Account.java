package com.bank.model;

/**
 * ===================================================================
 * МОДЕЛЬ БАНКОВСКОГО СЧЕТА
 * ===================================================================
 * 
 * Этот класс представляет сущность "Банковский счет" в банковской системе.
 * Содержит основную информацию о счете и его состоянии.
 * 
 * Основные характеристики:
 * - Уникальный идентификатор счета (ID)
 * - Имя владельца счета
 * - Текущий баланс счета
 * 
 * Класс реализует паттерн JavaBean с геттерами и сеттерами
 * для корректной работы с ORM фреймворками и сериализацией.
 * 
 * Особенности банковского счета:
 * - Баланс не может быть отрицательным (проверка в бизнес-логике)
 * - Каждый счет имеет уникального владельца
 * - ID генерируется автоматически при создании в БД
 * 
 * @author Банковское приложение
 * @version 1.0
 * @since 2024
 */
public class Account {
    
    // ===================================================================
    // ПОЛЯ КЛАССА (ПРИВАТНЫЕ СВОЙСТВА)
    // ===================================================================
    
    /**
     * Уникальный идентификатор банковского счета
     * Генерируется автоматически при создании в базе данных
     * Используется для однозначной идентификации счета в системе
     */
    private int id;
    
    /**
     * Имя владельца банковского счета
     * Обязательное поле, не может быть null или пустой строкой
     * Представляет физическое или юридическое лицо - владельца счета
     */
    private String ownerName;
    
    /**
     * Текущий баланс банковского счета
     * Выражается в денежных единицах (рубли, доллары и т.д.)
     * В реальной банковской системе должен быть BigDecimal для точности
     * В данном примере используется double для простоты
     */
    private double balance;

    // ===================================================================
    // КОНСТРУКТОРЫ
    // ===================================================================
    
    /**
     * Конструктор по умолчанию (без параметров)
     * 
     * Создает пустой объект Account с инициализацией полей значениями по умолчанию.
     * Используется фреймворками (например, Hibernate) для создания объектов
     * из данных базы данных.
     * 
     * Все поля инициализируются значениями по умолчанию:
     * - id = 0
     * - ownerName = null
     * - balance = 0.0
     */
    public Account() {
        // Пустой конструктор для фреймворков
        // Поля инициализируются значениями по умолчанию
    }

    /**
     * Полный конструктор со всеми параметрами
     * 
     * Используется для создания объекта Account со всеми данными,
     * включая ID (например, при загрузке из базы данных).
     * 
     * @param id уникальный идентификатор счета
     * @param ownerName имя владельца счета
     * @param balance текущий баланс счета
     */
    public Account(int id, String ownerName, double balance) {
        this.id = id;
        this.ownerName = ownerName;
        this.balance = balance;
    }

    /**
     * Конструктор для создания нового счета (без ID)
     * 
     * Используется при создании нового банковского счета.
     * ID будет присвоен автоматически при сохранении в базу данных.
     * 
     * @param ownerName имя владельца счета (не может быть null или пустой)
     * @param balance начальный баланс счета (должен быть >= 0)
     */
    public Account(String ownerName, double balance) {
        this.ownerName = ownerName;
        this.balance = balance;
    }

    // ===================================================================
    // ГЕТТЕРЫ И СЕТТЕРЫ (ACCESSOR METHODS)
    // ===================================================================
    // Геттеры и сеттеры обеспечивают инкапсуляцию данных
    // и позволяют контролировать доступ к полям класса

    /**
     * Получить уникальный идентификатор счета
     * 
     * @return ID счета
     */
    public int getId() {
        return id;
    }

    /**
     * Установить уникальный идентификатор счета
     * 
     * @param id новый ID счета
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * Получить имя владельца счета
     * 
     * @return имя владельца счета
     */
    public String getOwnerName() {
        return ownerName;
    }

    /**
     * Установить имя владельца счета
     * 
     * @param ownerName новое имя владельца счета
     */
    public void setOwnerName(String ownerName) {
        this.ownerName = ownerName;
    }

    /**
     * Получить текущий баланс счета
     * 
     * @return текущий баланс счета
     */
    public double getBalance() {
        return balance;
    }

    /**
     * Установить баланс счета
     * 
     * ВНИМАНИЕ: В реальной банковской системе изменение баланса
     * должно происходить только через специальные методы (депозит, снятие)
     * с проверкой бизнес-правил и логированием операций.
     * 
     * @param balance новый баланс счета
     */
    public void setBalance(double balance) {
        this.balance = balance;
    }

    // ===================================================================
    // ПЕРЕОПРЕДЕЛЕННЫЕ МЕТОДЫ ОБЪЕКТА
    // ===================================================================

    /**
     * Строковое представление объекта Account
     * 
     * Используется для логирования и отладки.
     * Возвращает читаемое представление всех полей объекта.
     * 
     * @return строковое представление банковского счета
     */
    @Override
    public String toString() {
        return "Account{" +
                "id=" + id +
                ", ownerName='" + ownerName + '\'' +
                ", balance=" + balance +
                '}';
    }
}
