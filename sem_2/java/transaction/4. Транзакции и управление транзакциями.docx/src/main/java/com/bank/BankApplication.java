package com.bank;

import com.bank.ui.BankConsole;

/**
 * ===================================================================
 * ГЛАВНЫЙ КЛАСС БАНКОВСКОГО ПРИЛОЖЕНИЯ
 * ===================================================================
 * 
 * Этот класс является точкой входа в банковское приложение для
 * демонстрации работы с транзакциями и управления банковскими счетами.
 * 
 * Основные функции:
 * - Инициализация банковского приложения
 * - Запуск консольного интерфейса
 * - Обработка критических ошибок
 * - Демонстрация работы с транзакциями в банковской системе
 * 
 * Архитектура приложения:
 * - Model: Account (модель банковского счета)
 * - Service: AccountService (бизнес-логика для работы со счетами)
 * - Database: DatabaseManager (управление подключением к БД)
 * - UI: BankConsole (консольный интерфейс)
 * 
 * Особенности:
 * - Использует H2 in-memory базу данных для простоты демонстрации
 * - Демонстрирует работу с транзакциями при переводах денег
 * - Показывает принципы ACID (Atomicity, Consistency, Isolation, Durability)
 * 
 * @author Банковское приложение
 * @version 1.0
 * @since 2024
 */
public class BankApplication {
    
    /**
     * ===================================================================
     * ГЛАВНЫЙ МЕТОД БАНКОВСКОГО ПРИЛОЖЕНИЯ
     * ===================================================================
     * 
     * Точка входа в банковское приложение. Выполняет инициализацию
     * и запуск консольного интерфейса для работы с банковскими счетами.
     * 
     * Последовательность выполнения:
     * 1. Создание консольного интерфейса
     * 2. Запуск основного цикла приложения
     * 3. Обработка критических ошибок
     * 
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        // Логирование запуска банковского приложения
        System.out.println("=== Банковское приложение ===");
        System.out.println("Запуск системы управления банковскими счетами...");
        
        try {
            // ===================================================================
            // ИНИЦИАЛИЗАЦИЯ КОНСОЛЬНОГО ИНТЕРФЕЙСА
            // ===================================================================
            System.out.println("Инициализация консольного интерфейса...");
            
            // Создание экземпляра консольного интерфейса
            // BankConsole содержит всю логику пользовательского интерфейса
            BankConsole console = new BankConsole();
            
            System.out.println("Консольный интерфейс успешно инициализирован");
            
            // ===================================================================
            // ЗАПУСК ОСНОВНОГО ЦИКЛА ПРИЛОЖЕНИЯ
            // ===================================================================
            System.out.println("Запуск основного цикла приложения...");
            
            // Запуск основного цикла работы с пользователем
            // Включает меню, обработку команд и взаимодействие с базой данных
            console.run();
            
            System.out.println("Основной цикл приложения завершен");
            
        } catch (Exception e) {
            // ===================================================================
            // ОБРАБОТКА КРИТИЧЕСКИХ ОШИБОК
            // ===================================================================
            // Вывод сообщения об ошибке пользователю
            System.err.println("Критическая ошибка приложения: " + e.getMessage());
            
            // Вывод полного стека вызовов для диагностики
            System.err.println("Детали ошибки:");
            e.printStackTrace();
            
            // В реальном банковском приложении здесь должно быть:
            // - Логирование ошибки в файл
            // - Уведомление администраторов
            // - Сохранение состояния для восстановления
            // - Безопасное завершение всех транзакций
            
            System.err.println("\nПриложение будет завершено из-за критической ошибки.");
        }
        
        // Завершение работы приложения
        System.out.println("\nБанковское приложение завершено");
    }
}
