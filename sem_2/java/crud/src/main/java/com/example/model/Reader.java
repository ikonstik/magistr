package com.example.model;

import java.util.Objects;

/**
 * ===================================================================
 * МОДЕЛЬ ЧИТАТЕЛЯ ДЛЯ СИСТЕМЫ УПРАВЛЕНИЯ БИБЛИОТЕКОЙ
 * ===================================================================
 * 
 * Этот класс представляет сущность "Читатель" в системе управления библиотекой.
 * Содержит персональную информацию о читателях библиотеки.
 * 
 * Основные характеристики:
 * - Уникальный идентификатор (ID)
 * - Имя читателя
 * - Фамилия читателя
 * - Email адрес (уникальный)
 * 
 * Класс реализует паттерн JavaBean с геттерами и сеттерами,
 * а также переопределяет методы equals(), hashCode() и toString()
 * для корректной работы с коллекциями и логированием.
 * 
 * @author Система управления библиотекой
 * @version 1.0
 * @since 2024
 */
public class Reader {
    
    // ===================================================================
    // ПОЛЯ КЛАССА (ПРИВАТНЫЕ СВОЙСТВА)
    // ===================================================================
    
    /**
     * Уникальный идентификатор читателя в базе данных
     * Может быть null для новых читателей, которые еще не сохранены в БД
     * После сохранения в БД получает значение от AUTO_INCREMENT
     */
    private Integer id;
    
    /**
     * Имя читателя
     * Обязательное поле, не может быть null или пустой строкой
     * Максимальная длина ограничена в базе данных (50 символов)
     */
    private String firstName;
    
    /**
     * Фамилия читателя
     * Обязательное поле, не может быть null или пустой строкой
     * Максимальная длина ограничена в базе данных (50 символов)
     */
    private String lastName;
    
    /**
     * Email адрес читателя
     * Обязательное поле, должно быть уникальным в системе
     * Используется для идентификации читателя и связи с ним
     * Максимальная длина ограничена в базе данных (100 символов)
     */
    private String email;

    // ===================================================================
    // КОНСТРУКТОРЫ
    // ===================================================================
    
    /**
     * Конструктор по умолчанию (без параметров)
     * 
     * Создает пустой объект Reader с инициализацией полей значениями по умолчанию.
     * Используется фреймворками (например, Hibernate) для создания объектов
     * из данных базы данных.
     * 
     * Все поля инициализируются как null
     */
    public Reader() {
        // Пустой конструктор для фреймворков
        // Поля инициализируются значениями по умолчанию
    }

    /**
     * Конструктор для создания нового читателя (без ID)
     * 
     * Используется при создании нового читателя, который еще не сохранен в БД.
     * ID будет присвоен автоматически при сохранении в базу данных.
     * 
     * @param firstName имя читателя (не может быть null или пустой)
     * @param lastName фамилия читателя (не может быть null или пустой)
     * @param email email адрес читателя (не может быть null или пустой, должен быть уникальным)
     */
    public Reader(String firstName, String lastName, String email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
    }

    /**
     * Полный конструктор со всеми параметрами
     * 
     * Используется для создания объекта Reader со всеми данными,
     * включая ID (например, при загрузке из базы данных).
     * 
     * @param id уникальный идентификатор читателя
     * @param firstName имя читателя
     * @param lastName фамилия читателя
     * @param email email адрес читателя
     */
    public Reader(Integer id, String firstName, String lastName, String email) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
    }

    // ===================================================================
    // ГЕТТЕРЫ И СЕТТЕРЫ (ACCESSOR METHODS)
    // ===================================================================
    // Геттеры и сеттеры обеспечивают инкапсуляцию данных
    // и позволяют контролировать доступ к полям класса

    /**
     * Получить уникальный идентификатор читателя
     * 
     * @return ID читателя или null, если читатель не сохранен в БД
     */
    public Integer getId() {
        return id;
    }

    /**
     * Установить уникальный идентификатор читателя
     * 
     * @param id новый ID читателя
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * Получить имя читателя
     * 
     * @return имя читателя
     */
    public String getFirstName() {
        return firstName;
    }

    /**
     * Установить имя читателя
     * 
     * @param firstName новое имя читателя
     */
    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    /**
     * Получить фамилию читателя
     * 
     * @return фамилия читателя
     */
    public String getLastName() {
        return lastName;
    }

    /**
     * Установить фамилию читателя
     * 
     * @param lastName новая фамилия читателя
     */
    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    /**
     * Получить email адрес читателя
     * 
     * @return email адрес читателя
     */
    public String getEmail() {
        return email;
    }

    /**
     * Установить email адрес читателя
     * 
     * @param email новый email адрес читателя
     */
    public void setEmail(String email) {
        this.email = email;
    }

    // ===================================================================
    // ПЕРЕОПРЕДЕЛЕННЫЕ МЕТОДЫ ОБЪЕКТА
    // ===================================================================

    /**
     * Строковое представление объекта Reader
     * 
     * Используется для логирования и отладки.
     * Возвращает читаемое представление всех полей объекта.
     * 
     * @return строковое представление читателя
     */
    @Override
    public String toString() {
        return "Reader{" +
                "id=" + id +
                ", firstName='" + firstName + '\'' +
                ", lastName='" + lastName + '\'' +
                ", email='" + email + '\'' +
                '}';
    }

    /**
     * Сравнение объектов Reader на равенство
     * 
     * Два читателя считаются равными, если все их поля совпадают.
     * Используется для сравнения объектов в коллекциях и при поиске.
     * 
     * @param o объект для сравнения
     * @return true если объекты равны, false в противном случае
     */
    @Override
    public boolean equals(Object o) {
        // Проверка на ссылочное равенство (быстрая проверка)
        if (this == o) return true;
        
        // Проверка на null и совместимость типов
        if (o == null || getClass() != o.getClass()) return false;
        
        // Приведение типа и сравнение полей
        Reader reader = (Reader) o;
        return Objects.equals(id, reader.id) &&
                Objects.equals(firstName, reader.firstName) &&
                Objects.equals(lastName, reader.lastName) &&
                Objects.equals(email, reader.email);
    }

    /**
     * Вычисление хеш-кода объекта
     * 
     * Возвращает хеш-код, основанный на всех полях объекта.
     * Важно для корректной работы с HashMap, HashSet и другими
     * коллекциями, использующими хеширование.
     * 
     * @return хеш-код объекта
     */
    @Override
    public int hashCode() {
        return Objects.hash(id, firstName, lastName, email);
    }
}

