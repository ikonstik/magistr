package com.example.model;

import java.util.Objects;

/**
 * ===================================================================
 * МОДЕЛЬ КНИГИ ДЛЯ СИСТЕМЫ УПРАВЛЕНИЯ БИБЛИОТЕКОЙ
 * ===================================================================
 * 
 * Этот класс представляет сущность "Книга" в системе управления библиотекой.
 * Содержит все необходимые поля для описания книги и её состояния.
 * 
 * Основные характеристики:
 * - Уникальный идентификатор (ID)
 * - Название книги
 * - Автор книги
 * - Год издания
 * - Статус доступности (выдана/доступна)
 * 
 * Класс реализует паттерн JavaBean с геттерами и сеттерами,
 * а также переопределяет методы equals(), hashCode() и toString()
 * для корректной работы с коллекциями и логированием.
 * 
 * @author Система управления библиотекой
 * @version 1.0
 * @since 2024
 */
public class Book {
    
    // ===================================================================
    // ПОЛЯ КЛАССА (ПРИВАТНЫЕ СВОЙСТВА)
    // ===================================================================
    
    /**
     * Уникальный идентификатор книги в базе данных
     * Может быть null для новых книг, которые еще не сохранены в БД
     * После сохранения в БД получает значение от AUTO_INCREMENT
     */
    private Integer id;
    
    /**
     * Название книги
     * Обязательное поле, не может быть null или пустой строкой
     * Максимальная длина ограничена в базе данных (100 символов)
     */
    private String title;
    
    /**
     * Автор книги
     * Обязательное поле, не может быть null или пустой строкой
     * Максимальная длина ограничена в базе данных (100 символов)
     */
    private String author;
    
    /**
     * Год издания книги
     * Может быть null, если год издания неизвестен
     * Обычно представляет собой четырехзначное число (например, 2024)
     */
    private Integer year;
    
    /**
     * Статус доступности книги
     * true - книга доступна для выдачи
     * false - книга выдана читателю
     * По умолчанию true (новая книга всегда доступна)
     */
    private Boolean isAvailable;

    // ===================================================================
    // КОНСТРУКТОРЫ
    // ===================================================================
    
    /**
     * Конструктор по умолчанию (без параметров)
     * 
     * Создает пустой объект Book с инициализацией полей значениями по умолчанию.
     * Используется фреймворками (например, Hibernate) для создания объектов
     * из данных базы данных.
     * 
     * Все поля инициализируются как null, кроме isAvailable = true
     */
    public Book() {
        // Пустой конструктор для фреймворков
        // Поля инициализируются значениями по умолчанию
    }

    /**
     * Конструктор для создания новой книги (без ID)
     * 
     * Используется при создании новой книги, которая еще не сохранена в БД.
     * ID будет присвоен автоматически при сохранении в базу данных.
     * 
     * @param title название книги (не может быть null или пустой)
     * @param author автор книги (не может быть null или пустой)
     * @param year год издания (может быть null)
     */
    public Book(String title, String author, Integer year) {
        this.title = title;
        this.author = author;
        this.year = year;
        // По умолчанию новая книга всегда доступна для выдачи
        this.isAvailable = true;
    }

    /**
     * Полный конструктор со всеми параметрами
     * 
     * Используется для создания объекта Book со всеми данными,
     * включая ID (например, при загрузке из базы данных).
     * 
     * @param id уникальный идентификатор книги
     * @param title название книги
     * @param author автор книги
     * @param year год издания
     * @param isAvailable статус доступности книги
     */
    public Book(Integer id, String title, String author, Integer year, Boolean isAvailable) {
        this.id = id;
        this.title = title;
        this.author = author;
        this.year = year;
        this.isAvailable = isAvailable;
    }

    // ===================================================================
    // ГЕТТЕРЫ И СЕТТЕРЫ (ACCESSOR METHODS)
    // ===================================================================
    // Геттеры и сеттеры обеспечивают инкапсуляцию данных
    // и позволяют контролировать доступ к полям класса

    /**
     * Получить уникальный идентификатор книги
     * 
     * @return ID книги или null, если книга не сохранена в БД
     */
    public Integer getId() {
        return id;
    }

    /**
     * Установить уникальный идентификатор книги
     * 
     * @param id новый ID книги
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * Получить название книги
     * 
     * @return название книги
     */
    public String getTitle() {
        return title;
    }

    /**
     * Установить название книги
     * 
     * @param title новое название книги
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * Получить автора книги
     * 
     * @return автор книги
     */
    public String getAuthor() {
        return author;
    }

    /**
     * Установить автора книги
     * 
     * @param author новый автор книги
     */
    public void setAuthor(String author) {
        this.author = author;
    }

    /**
     * Получить год издания книги
     * 
     * @return год издания или null, если неизвестен
     */
    public Integer getYear() {
        return year;
    }

    /**
     * Установить год издания книги
     * 
     * @param year новый год издания
     */
    public void setYear(Integer year) {
        this.year = year;
    }

    /**
     * Проверить, доступна ли книга для выдачи
     * 
     * @return true если книга доступна, false если выдана
     */
    public Boolean getIsAvailable() {
        return isAvailable;
    }

    /**
     * Установить статус доступности книги
     * 
     * @param isAvailable новый статус доступности
     */
    public void setIsAvailable(Boolean isAvailable) {
        this.isAvailable = isAvailable;
    }

    // ===================================================================
    // ПЕРЕОПРЕДЕЛЕННЫЕ МЕТОДЫ ОБЪЕКТА
    // ===================================================================

    /**
     * Строковое представление объекта Book
     * 
     * Используется для логирования и отладки.
     * Возвращает читаемое представление всех полей объекта.
     * 
     * @return строковое представление книги
     */
    @Override
    public String toString() {
        return "Book{" +
                "id=" + id +
                ", title='" + title + '\'' +
                ", author='" + author + '\'' +
                ", year=" + year +
                ", isAvailable=" + isAvailable +
                '}';
    }

    /**
     * Сравнение объектов Book на равенство
     * 
     * Две книги считаются равными, если все их поля совпадают.
     * Используется для сравнения объектов в коллекциях и при поиске.
     * 
     * @param o объект для сравнения
     * @return true если объекты равны, false в противном случае
     */
    @Override
    public boolean equals(Object o) {
        // Проверка на ссылочное равенство (быстрая проверка)
        if (this == o) return true;
        
        // Проверка на null и совместимость типов
        if (o == null || getClass() != o.getClass()) return false;
        
        // Приведение типа и сравнение полей
        Book book = (Book) o;
        return Objects.equals(id, book.id) &&
                Objects.equals(title, book.title) &&
                Objects.equals(author, book.author) &&
                Objects.equals(year, book.year) &&
                Objects.equals(isAvailable, book.isAvailable);
    }

    /**
     * Вычисление хеш-кода объекта
     * 
     * Возвращает хеш-код, основанный на всех полях объекта.
     * Важно для корректной работы с HashMap, HashSet и другими
     * коллекциями, использующими хеширование.
     * 
     * @return хеш-код объекта
     */
    @Override
    public int hashCode() {
        return Objects.hash(id, title, author, year, isAvailable);
    }
}

