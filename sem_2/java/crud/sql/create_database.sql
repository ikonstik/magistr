-- ===================================================================
-- СОЗДАНИЕ БАЗЫ ДАННЫХ ДЛЯ СИСТЕМЫ УПРАВЛЕНИЯ БИБЛИОТЕКОЙ
-- ===================================================================
-- Этот скрипт создает полноценную базу данных для системы управления библиотекой
-- Включает таблицы для книг, читателей и выдачи книг
-- Предназначен для демонстрации CRUD операций через JDBC
-- ===================================================================

-- Создание базы данных library_db
-- IF NOT EXISTS предотвращает ошибку, если база уже существует
-- CHARACTER SET utf8mb4 обеспечивает поддержку всех Unicode символов
-- COLLATE utf8mb4_unicode_ci обеспечивает корректную сортировку
CREATE DATABASE IF NOT EXISTS library_db 
CHARACTER SET utf8mb4 
COLLATE utf8mb4_unicode_ci;

-- Переключение на созданную базу данных
-- Все последующие команды будут выполняться в контексте этой БД
USE library_db;

-- ===================================================================
-- СОЗДАНИЕ ТАБЛИЦЫ BOOKS (КНИГИ)
-- ===================================================================
-- Основная таблица для хранения информации о книгах в библиотеке
-- Содержит метаданные о книгах и их доступности
CREATE TABLE IF NOT EXISTS books (
    -- Автоинкрементный первичный ключ
    id INT AUTO_INCREMENT PRIMARY KEY,
    
    -- Название книги (обязательное поле, максимум 100 символов)
    title VARCHAR(100) NOT NULL COMMENT 'Название книги',
    
    -- Автор книги (обязательное поле, максимум 100 символов)
    author VARCHAR(100) NOT NULL COMMENT 'Автор книги',
    
    -- Год издания (необязательное поле)
    year INT COMMENT 'Год издания',
    
    -- Статус доступности книги (по умолчанию TRUE - доступна)
    -- BOOLEAN хранит TRUE/FALSE значения
    is_available BOOLEAN DEFAULT TRUE COMMENT 'Доступна ли книга для выдачи'
) ENGINE=InnoDB 
  DEFAULT CHARSET=utf8mb4 
  COLLATE=utf8mb4_unicode_ci
  COMMENT='Таблица книг в библиотеке';

-- ===================================================================
-- СОЗДАНИЕ ТАБЛИЦЫ READERS (ЧИТАТЕЛИ)
-- ===================================================================
-- Таблица для хранения информации о читателях библиотеки
-- Содержит персональные данные читателей
CREATE TABLE IF NOT EXISTS readers (
    -- Автоинкрементный первичный ключ
    id INT AUTO_INCREMENT PRIMARY KEY,
    
    -- Имя читателя (обязательное поле, максимум 50 символов)
    first_name VARCHAR(50) NOT NULL COMMENT 'Имя читателя',
    
    -- Фамилия читателя (обязательное поле, максимум 50 символов)
    last_name VARCHAR(50) NOT NULL COMMENT 'Фамилия читателя',
    
    -- Email читателя (уникальный, обязательный, максимум 100 символов)
    -- UNIQUE обеспечивает уникальность email адресов
    email VARCHAR(100) UNIQUE NOT NULL COMMENT 'Email читателя (уникальный)'
) ENGINE=InnoDB 
  DEFAULT CHARSET=utf8mb4 
  COLLATE=utf8mb4_unicode_ci
  COMMENT='Таблица читателей библиотеки';

-- ===================================================================
-- СОЗДАНИЕ ТАБЛИЦЫ BOOK_LOANS (ВЫДАЧИ КНИГ)
-- ===================================================================
-- Связующая таблица для отслеживания выдачи книг читателям
-- Реализует связь "многие ко многим" между книгами и читателями
-- Включает временные метки для контроля сроков выдачи
CREATE TABLE IF NOT EXISTS book_loans (
    -- Автоинкрементный первичный ключ
    id INT AUTO_INCREMENT PRIMARY KEY,
    
    -- ID книги (внешний ключ, обязательное поле)
    book_id INT NOT NULL COMMENT 'ID книги',
    
    -- ID читателя (внешний ключ, обязательное поле)
    reader_id INT NOT NULL COMMENT 'ID читателя',
    
    -- Дата выдачи книги (обязательное поле)
    loan_date DATE NOT NULL COMMENT 'Дата выдачи книги',
    
    -- Дата возврата книги (необязательное поле, NULL если книга не возвращена)
    return_date DATE NULL COMMENT 'Дата возврата книги',
    
    -- ===================================================================
    -- ВНЕШНИЕ КЛЮЧИ ДЛЯ ОБЕСПЕЧЕНИЯ ЦЕЛОСТНОСТИ ДАННЫХ
    -- ===================================================================
    
    -- Внешний ключ на таблицу books
    -- ON DELETE CASCADE означает, что при удалении книги
    -- автоматически удаляются все записи о её выдаче
    FOREIGN KEY (book_id) REFERENCES books(id) ON DELETE CASCADE,
    
    -- Внешний ключ на таблицу readers
    -- ON DELETE CASCADE означает, что при удалении читателя
    -- автоматически удаляются все записи о его выдачах
    FOREIGN KEY (reader_id) REFERENCES readers(id) ON DELETE CASCADE,
    
    -- ===================================================================
    -- ИНДЕКСЫ ДЛЯ ОПТИМИЗАЦИИ ПРОИЗВОДИТЕЛЬНОСТИ
    -- ===================================================================
    
    -- Индекс по ID книги для быстрого поиска выдач по книге
    INDEX idx_book_loans_book_id (book_id),
    
    -- Индекс по ID читателя для быстрого поиска выдач по читателю
    INDEX idx_book_loans_reader_id (reader_id),
    
    -- Индекс по дате выдачи для сортировки и фильтрации
    INDEX idx_book_loans_loan_date (loan_date),
    
    -- Индекс по дате возврата для поиска возвращенных книг
    INDEX idx_book_loans_return_date (return_date)
) ENGINE=InnoDB 
  DEFAULT CHARSET=utf8mb4 
  COLLATE=utf8mb4_unicode_ci
  COMMENT='Таблица выдачи книг читателям';

-- ===================================================================
-- СОЗДАНИЕ ДОПОЛНИТЕЛЬНЫХ ИНДЕКСОВ ДЛЯ ОПТИМИЗАЦИИ
-- ===================================================================
-- Индексы ускоряют поиск и сортировку данных
-- Особенно важны для часто используемых полей

-- Индекс по названию книги для быстрого поиска книг по названию
CREATE INDEX idx_books_title ON books(title);

-- Индекс по автору книги для быстрого поиска книг по автору
CREATE INDEX idx_books_author ON books(author);

-- Индекс по статусу доступности для быстрого поиска доступных/недоступных книг
CREATE INDEX idx_books_is_available ON books(is_available);

-- Индекс по email читателя для быстрого поиска читателей по email
CREATE INDEX idx_readers_email ON readers(email);

-- Составной индекс по фамилии и имени читателя для сортировки
CREATE INDEX idx_readers_name ON readers(last_name, first_name);

