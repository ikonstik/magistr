-- ===================================================================
-- ДОПОЛНИТЕЛЬНЫЕ АНАЛИТИЧЕСКИЕ ЗАПРОСЫ
-- ===================================================================

-- Книги, которые сейчас выданы (с информацией о читателях)
SELECT 
    'Книги в выдаче:' as info,
    b.title as 'Название книги',
    b.author as 'Автор',
    r.first_name as 'Имя читателя',
    r.last_name as 'Фамилия читателя',
    bl.loan_date as 'Дата выдачи'
FROM books b
JOIN book_loans bl ON b.id = bl.book_id
JOIN readers r ON bl.reader_id = r.id
WHERE bl.return_date IS NULL
ORDER BY bl.loan_date;

-- Читатели с количеством взятых книг
SELECT 
    'Активность читателей:' as info,
    r.first_name as 'Имя',
    r.last_name as 'Фамилия',
    COUNT(bl.id) as 'Количество взятых книг'
FROM readers r
LEFT JOIN book_loans bl ON r.id = bl.reader_id
GROUP BY r.id, r.first_name, r.last_name
ORDER BY COUNT(bl.id) DESC;

-- Популярность книг (количество выдач)
SELECT 
    'Популярность книг:' as info,
    b.title as 'Название книги',
    b.author as 'Автор',
    COUNT(bl.id) as 'Количество выдач'
FROM books b
LEFT JOIN book_loans bl ON b.id = bl.book_id
GROUP BY b.id, b.title, b.author
ORDER BY COUNT(bl.id) DESC;

